    public DoctorAppointmentsForm(Doctor thisDoctor) {
        initComponents();
        this.setLocationRelativeTo(null);
        
        this.userDoctor = thisDoctor;
        
        DefaultListModel mod = new DefaultListModel();
        appointmentList.setModel(mod);
        
        for (int i = 0; i < this.userDoctor.doctorAppointmentList.size(); i++) 
        {
            
            mod.addElement(this.userDoctor.doctorAppointmentList.get(i).getDate() + " (" + this.userDoctor.doctorAppointmentList.get(i).getPatient().getId() + " - " + this.userDoctor.doctorAppointmentList.get(i).getPatient().getName() + ")");
        }
        
        
        
        DefaultComboBoxModel comboMod = new DefaultComboBoxModel();
        patientComboBox.setModel(comboMod);
        
        for (int i = 0; i < LoginStrategyController.patientList.size(); i++) {
            
            comboMod.addElement(LoginStrategyController.patientList.get(i).getName());
        }
    }

