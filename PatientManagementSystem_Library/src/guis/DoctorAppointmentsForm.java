    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed

        String date = dateTextField.getText();
        String patientName = patientComboBox.getSelectedItem().toString();
        Patient patientSelected = null;

        for (int i = 0; i < LoginStrategyController.patientList.size(); i++)
        {
            if (LoginStrategyController.patientList.get(i).getName() == patientName)
            {
                patientSelected = LoginStrategyController.patientList.get(i);
            }
        }

        this.userDoctor.proposeAppointment(this.userDoctor, date, patientSelected);
        System.out.println(this.userDoctor.getName() + " proposed to see " + patientSelected.getName() + " on " + date);

        this.setDefaultCloseOperation(RequestPatientAccountForm.DISPOSE_ON_CLOSE);
        this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
    }//GEN-LAST:event_submitButtonActionPerformed

    

