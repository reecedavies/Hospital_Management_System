    private void createAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccountButtonActionPerformed
        // TODO add your handling code here:

        RequestedPatient selectedRequest = null;
        
        for (int i = 0; i < this.userSecretary.requestedPatientList.size(); i++)
        {
            if (this.userSecretary.requestedPatientList.get(i).getEmail() == requestList.getSelectedValue())
            {
                selectedRequest = this.userSecretary.requestedPatientList.get(i);
            }
        }

        this.userSecretary.approvePatientAccount(selectedRequest);
        
        
        JOptionPane.showMessageDialog(null, "Account Added", "Alert", JOptionPane.OK_OPTION);
        DefaultListModel newMod = new DefaultListModel();
        requestList.setModel(newMod);

        for (int q = 0; q < this.userSecretary.requestedPatientList.size(); q++)
        {
            newMod.addElement(this.userSecretary.requestedPatientList.get(q).getEmail());
        }
    }//GEN-LAST:event_createAccountButtonActionPerformed

