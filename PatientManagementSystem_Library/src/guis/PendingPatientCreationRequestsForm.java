    private void viewDetailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsButtonActionPerformed
        // TODO add your handling code here:

        for (int i = 0; i < this.userSecretary.requestedPatientList.size(); i++)
        {
            if (this.userSecretary.requestedPatientList.get(i).getEmail() == requestList.getSelectedValue())
            {
                nameLabel.setText(this.userSecretary.requestedPatientList.get(i).getName());
                emailLabel.setText(this.userSecretary.requestedPatientList.get(i).getEmail());
                ageLabel.setText(Integer.toString(this.userSecretary.requestedPatientList.get(i).getAge()));
                addressLabel.setText(this.userSecretary.requestedPatientList.get(i).getAddress());
            }
        }

    }//GEN-LAST:event_viewDetailsButtonActionPerformed

    

