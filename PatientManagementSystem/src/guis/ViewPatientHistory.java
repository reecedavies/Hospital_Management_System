    public ViewPatientHistory(Patient thisPatient) {
        initComponents();
        this.setLocationRelativeTo(null);
        
        this.userPatient = thisPatient;
        
        DefaultListModel mod = new DefaultListModel();
        historyList.setModel(mod);
        
        for (int i = 0; i < this.userPatient.patientHistoryList.size(); i++) {
            
            //doctorAccountList.add(i+1, "Item");
            mod.addElement(this.userPatient.patientHistoryList.get(i).getDate() + " - " + this.userPatient.patientHistoryList.get(i).getComment());
        }
        
    }

